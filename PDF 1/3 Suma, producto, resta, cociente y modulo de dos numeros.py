a = int(input("Ingrese un numero: "))
b = int(input("Ingrese un numero: "))

suma = a + b
producto = a * b
resta = a - b
cociente = a // b
modulo = a - (cociente * b)

print ("Suma: " + str(suma))
print("Producto: " + str(producto))
print("Resta: " + str(resta))
print("Cociente: " + str(cociente))
print("Modulo: " + str(modulo))
